
static v2
make_v2(float x, float y)
{
	v2 result;

	result.x = x;
	result.y = y;
	return result;
}

static v3
make_v3(float x, float y, float z)
{
	v3 result;

	result.x = x;
	result.y = y;
	result.z = z;
	return result;
}

static v4
make_v4(float x, float y, float z, float w)
{
	v4 result;

	result.x = x;
	result.y = y;
	result.z = z;
	result.w = w;
	return result;
}
